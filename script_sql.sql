/* CREACIÓN DE TABLAS */
CREATE TABLE carrera(
cod_carrera SERIAL NOT NULL,
nombre VARCHAR(100) NOT NULL,
estado VARCHAR(50) NOT NULL,
PRIMARY KEY(cod_carrera)
);

